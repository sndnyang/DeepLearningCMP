class VGG(nn.Module):

    def __init__(self, features, input_shape=(3, 32, 32), num_classes=1000, init_weights=True, batchnorm=True):
        super(VGG, self).__init__()
        self.features = features
        self.batchnorm = batchnorm
        input_size = self._get_conv_output_size(input_shape)
        self.dense = nn.Sequential(
            nn.Dropout(0.4),
            nn.Linear(input_size, 512),
        )
        self.bn = nn.BatchNorm1d(512)
        self.classifier = nn.Sequential(
            nn.ReLU(True),
            nn.Dropout(0.4),
            nn.Linear(512, num_classes)
        )
        # self.classifier = nn.Linear(input_size, num_classes)
        if init_weights:
            self._initialize_weights()

    def forward(self, x):
        x = self.features(x)
        x = x.view(x.size(0), -1)
        x = x.dense(x)
        if self.batchnorm:
            x = x.bn(x)
        x = self.classifier(x)
        return x

    def _get_conv_output_size(self, shape):
        bs = 1
        inputs = torch.rand(bs, *shape)
        output_feat = self.features(inputs)
        n_size = output_feat.data.view(bs, -1).size(1)
        return n_size

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
                m.weight.data.normal_(0, math.sqrt(2. / n))
                if m.bias is not None:
                    m.bias.data.zero_()
            elif isinstance(m, nn.BatchNorm2d):
                m.weight.data.fill_(1)
                m.bias.data.zero_()
            elif isinstance(m, nn.Linear):
                m.weight.data.normal_(0, 0.01)
                m.bias.data.zero_()


def make_layers(layer_cfg, batch_norm=False):
    layers = []
    in_channels = 3
    for v in layer_cfg:
        if v == 'M':
            layers += [nn.MaxPool2d(kernel_size=2, stride=2)]
        elif 'D' == v:
            layers += [nn.Dropout(0.3)]
        else:
            conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
            if batch_norm:
                layers += [conv2d, nn.BatchNorm2d(v), nn.ReLU(inplace=True)]
            else:
                layers += [conv2d, nn.ReLU(inplace=True)]
            in_channels = v
    return nn.Sequential(*layers)


cfg = {
    'A': [64, 'M', 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'B': [64, 64, 'M', 128, 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'D': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 'M', 512, 512, 512, 'M', 512, 512, 512, 'M'],
    'F': [64, 'D', 64, 'M', 128, 'D', 128, 'M', 256, 'D', 256, 'D', 256, 'M', 512, 'D', 512, 'D', 512, 'M', 512, 'D', 512, 'D', 512, 'M'],
    'E': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 256, 'M', 512, 512, 512, 512, 'M', 512, 512, 512, 512, 'M'],
}


def vgg11(pretrained=False, **kwargs):
    """VGG 11-layer model (configuration "A")

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
    """
    if pretrained:
        kwargs['init_weights'] = False
    model = VGG(make_layers(cfg['A']), **kwargs)
    if pretrained:
        model.load_state_dict(model_zoo.load_url(model_urls['vgg11']))
    return model


def vgg11_bn(pretrained=False, **kwargs):
    """VGG 11-layer model (configuration "A") with batch normalization

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
    """
    if pretrained:
        kwargs['init_weights'] = False
    model = VGG(make_layers(cfg['A'], batch_norm=True), **kwargs)
